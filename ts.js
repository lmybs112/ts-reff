const newFormattedData = {};

if (res && res.Item) {
    for (const brandKey in res.Item) {
        // 确保我们只处理对象自身的属性
        if (res.Item.hasOwnProperty(brandKey)) {
            const brandData = res.Item[brandKey];
            const brandName = brandKey; // 例如 "OLIVO", "ALMI"

            // 确保品牌数据中有 domain 和 routes
            if (brandData.domain && typeof brandData.domain === 'string' &&
                brandData.routes && Array.isArray(brandData.routes)) {

                const individualDomains = [];
                // 解析 domain 字符串，例如 "#fashion#,#clothes#"
                const domainParts = brandData.domain.split(',');
                domainParts.forEach(part => {
                    // 对每个部分按 '#' 分割，并过滤掉空字符串
                    // 例如 "#fashion#" -> ["fashion"]
                    part.split('#').filter(d => d.length > 0).forEach(domain => {
                        individualDomains.push(domain);
                    });
                });

                // 遍历品牌下的所有路由
                brandData.routes.forEach(route => {
                    // 确保路由对象包含必要的字段 (Name, Description, Route)
                    // 以避免处理像 {} 这样的空路由对象
                    if (route && route.Name && route.Description && route.Route) {
                        const transformedRoute = {
                            title: route.Name,
                            description: route.Description,
                            routeId: route.Route,
                            brand: brandName
                        };

                        // 将转换后的路由添加到其所属的每个 domain 中
                        individualDomains.forEach(domain => {
                            if (!newFormattedData[domain]) {
                                newFormattedData[domain] = { routes: [] };
                            }
                            newFormattedData[domain].routes.push(transformedRoute);
                        });
                    }
                });
            }
        }
    }
}

